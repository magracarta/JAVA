package day_01;

public class VariMain {
	public static void main(String[] args) {
		//int x  변수선언
		// 자료형 변수명;
		// int : 자료형
		// x   : 변수명
		
		int x = 5+8; // 변수의 선언
		
		x = 10; // 변수 x에 10을 대입한다. ==> 변수 x 공간에 10을 복사하여 넣는다.
		System.out.println(x);//x호출  : 변수x 에 들어있는 값을 복사하여 가져와 사용
	}
}
